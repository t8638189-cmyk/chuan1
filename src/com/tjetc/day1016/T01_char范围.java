package com.tjetc.day1016;

public class T01_char范围 {
        public static void main(String[] args) {

            //char的范围:最小值:\u0000
            char c='\u0000';
            System.out.println((int)c);
            //char 的范围:最大值:\uFFFF
            char c2='\uFFFF';
            System.out.println((int)c2);
        }
}
